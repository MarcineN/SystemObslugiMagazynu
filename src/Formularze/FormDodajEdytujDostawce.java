    private void przyciskDodajActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_przyciskDodajActionPerformed

        String id = textId.getText();
        String nazwa = poleNazwa.getText();
        String ulica = poleUlica.getText();
        String miasto = poleMiasto.getText();
        String kod_poczt = poleKodPoczt.getText();
        String wojewodztwo = poleWyborWojewodztwo.getSelectedItem().toString();
        String nip = poleNip.getText();

        // Walidacja danych
        //nazwa
        String bledy = "";
        WalidatorDanych WL = new WalidatorDanych();

        WL.sprawdz("Nazwa", nazwa, 4, 45);
        WL.sprawdz("Ulica", ulica, 4, 45);
        WL.sprawdz("Miasto", miasto, 3, 45);
        WL.sprawdz("Kod Pocztowy", kod_poczt, 6, 6);
        WL.sprawdz("NIP", nip, 7, 45);

        bledy += WL.getZnalezioneBledy();

        // sprawdzanie czy wystapily bledy
        if (!bledy.equals("")) {
            JOptionPane.showMessageDialog(this, bledy, "Błąd!", 2);
            WL.usunBledy();
        }

        if (bledy.equals("")) { // walidacja poprawna
            PreparedStatement dodajm = null;
            PreparedStatement pobwoj;
            ResultSet wynikwoj;
            try {
                con.setAutoCommit(false);

                String query3 = "SELECT kod_w FROM wojewodztwa WHERE wojewodztwo='" + wojewodztwo + "'";
                pobwoj = con.prepareStatement(query3);
                wynikwoj = pobwoj.executeQuery(query3);
                wynikwoj.next();
                String tmp = wynikwoj.getString(1);
                //System.out.println("TEST id_t " + tmp2);

                //System.out.println("TEST id_d " + tmp3);
                String query1 = "INSERT INTO dostawcy (id_d,nazwa, ulica, miasto, kod_pocztowy, nip, kod_w) VALUES (NULL,?,?,?,?,?,?)";
                String query2 = "UPDATE dostawcy SET nazwa=?, ulica=?, miasto=?, kod_pocztowy=?, nip=?, kod_w=? WHERE id_d=?";

                if (czy_edycja) {
                    dodajm = con.prepareStatement(query2);
                    dodajm.setInt(7, Integer.parseInt(id));
                }

                if (!czy_edycja) {
                    dodajm = con.prepareStatement(query1);
                }

                dodajm.setString(1, nazwa);
                dodajm.setString(2, ulica);
                dodajm.setString(3, miasto);
                dodajm.setString(4, kod_poczt);
                dodajm.setString(5, nip);
                dodajm.setString(6, tmp);

                dodajm.executeUpdate();

                con.commit();

                this.setVisible(false);
                FormGlowny.setEdycjaOtwarta(false);

            } catch (Exception e) {
                System.out.println(e);
                JOptionPane.showMessageDialog(this, "Wprowadzone dane sa bledne!", "Błąd!", 2);
            }
        }
    }//GEN-LAST:event_przyciskDodajActionPerformed

