    public void wypelnijDoEdycji() {

        DefaultTableModel model = (DefaultTableModel) jt.getModel();
        int wiersz = jt.getSelectedRow();

        textId2.setText("ID: ");
        textId.setText(model.getValueAt(wiersz, 0).toString());
        poleNazwa.setText(model.getValueAt(wiersz, 1).toString());
        poleUlica.setText(model.getValueAt(wiersz, 2).toString());
        poleMiasto.setText(model.getValueAt(wiersz, 3).toString());
        poleKodPoczt.setText(model.getValueAt(wiersz, 4).toString());
        poleWyborWojewodztwo.setSelectedItem(model.getValueAt(wiersz, 5).toString());

        poleNip.setText(model.getValueAt(wiersz, 6).toString());

    }

    public void wypelnijWoj() {
        try {
            PreparedStatement pobierzWoj;
            ResultSet wynikWoj;
            String query = "SELECT wojewodztwo FROM wojewodztwa";

            pobierzWoj = con.prepareStatement(query);
            wynikWoj = pobierzWoj.executeQuery(query);

            while (wynikWoj.next()) {
                poleWyborWojewodztwo.addItem(wynikWoj.getString(1));
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, e, "Błąd!", 2);
        }
    }

