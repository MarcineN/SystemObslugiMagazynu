    public boolean sprawdzStareHaslo(String haslo) {
        PreparedStatement sprawdzHaslo;
        ResultSet wynikHaslo;
        String query = "SELECT * FROM uzytkownicy WHERE login=? AND haslo=?";
        try {
            sprawdzHaslo = con.prepareStatement(query);
            sprawdzHaslo.setString(1, U.getLogin());
            sprawdzHaslo.setString(2, haslo);
            wynikHaslo = sprawdzHaslo.executeQuery();
            if (wynikHaslo.next()) {
                return true;
            }
        } catch (SQLException ex) {
            System.out.println(ex);
        }
        return false;
    }

