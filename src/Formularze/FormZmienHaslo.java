    private void przyciskDodajMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_przyciskDodajMouseClicked
        String nowyHash = SzyfrowanieMd5.zaszyfruj(poleNoweHaslo.getText());
        String staryHash = SzyfrowanieMd5.zaszyfruj(poleStareHaslo.getText());
        int idUzytk = U.getId();

        if (poleNoweHaslo.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Hasło nie może być puste!", "Błąd!", 2);
        } else if (nowyHash.equals(U.getHashHaslo())) {
            JOptionPane.showMessageDialog(this, "Nowe hasło musi być inne!", "Błąd!", 2);
        } else if (!sprawdzStareHaslo(staryHash)) {
            JOptionPane.showMessageDialog(this, "Stare hasło jest nieprawidłowe!", "Błąd!", 2);
        } else {
            Object[] opcje = {"Tak", "Nie"};
            int czy_napewno = JOptionPane.showOptionDialog(this, "Czy napewno chcesz zmienić hasło?\nZatwierdzenie wymusi przelogowanie!", "Zmiana loginu", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE, null, opcje, opcje[0]);

            if (czy_napewno == 0) {
                PreparedStatement zmienHaslo;
                String query = "UPDATE uzytkownicy SET haslo='" + nowyHash + "' WHERE id_u='" + idUzytk + "'";
                try {
                    zmienHaslo = con.prepareStatement(query);
                    zmienHaslo.executeUpdate();
                    FG.dispose();
                    this.setVisible(false);
                    FormLogowanie FL = new FormLogowanie();
                    FL.setPolaczenie(PolaczenieBaza.getPolaczenie());
                    FL.setBazaTekst();
                    FL.setVisible(true);
                } catch (SQLException ex) {
                    System.out.println(ex);
                }
            }
        }

    }//GEN-LAST:event_przyciskDodajMouseClicked

